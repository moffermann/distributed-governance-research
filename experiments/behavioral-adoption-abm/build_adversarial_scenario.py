"""Experiment C adapter: behavioral sweep means -> adversarial ABM scenario.

Implements the pre-registered mapping of OUTPUT_TO_ADVERSARIAL_ABM.md: replaces
the adversarial engine's imposed population block (and executor shares) with
behaviorally generated values, carrying full provenance. Everything else in the
base adversarial scenario (planning calibration, attacks, architectures,
fiscalizers) is left untouched, per the declared boundaries.

Usage:
    python build_adversarial_scenario.py --sweep outputs/sweep/sweep_summary.json \
        --source baseline --scenario-id behavioral-baseline
"""

from __future__ import annotations

import argparse
import json
from datetime import date
from pathlib import Path

ROOT = Path(__file__).resolve().parent
DEFAULT_BASE = ROOT.parent / "adversarial-abm" / "scenarios" / "baseline-medium.json"


def main() -> None:
    parser = argparse.ArgumentParser(description="Build an adversarial ABM scenario from behavioral sweep output.")
    parser.add_argument("--sweep", required=True, help="sweep_summary.json produced by run_sweep.py.")
    parser.add_argument("--source", required=True, help="Behavioral scenario_id inside the sweep summary.")
    parser.add_argument("--scenario-id", required=True, help="scenario_id for the generated adversarial scenario.")
    parser.add_argument("--base", default=str(DEFAULT_BASE), help="Base adversarial scenario to overlay.")
    parser.add_argument("--population", type=int, default=1000, help="Behavioral population size (for block-size math).")
    parser.add_argument("--output", default=None, help="Defaults to ../adversarial-abm/scenarios/<scenario-id>.json.")
    args = parser.parse_args()

    sweep = json.loads(Path(args.sweep).read_text(encoding="utf-8"))
    entry = next((s for s in sweep["scenarios"] if s["scenario_id"] == args.source), None)
    if entry is None:
        raise SystemExit(f"scenario '{args.source}' not found in {args.sweep}")
    m = {k: v["mean"] for k, v in entry["metrics"].items()}

    attentive = m["attentive_share_mean"]
    registered = m["registered_user_share"]
    salience = max(0.0, m["direct_participation_share"] * registered - attentive)
    profile = m["profile_driven_share"] * registered
    delegator = m["delegator_share"]
    passive = max(0.0, 1.0 - attentive - salience - profile - delegator)
    block_size = max(1, round(delegator * args.population / max(1.0, m["active_delegate_count"])))

    base = json.loads(Path(args.base).read_text(encoding="utf-8"))
    base["scenario_id"] = args.scenario_id
    base["description"] = (
        f"Experiment C integrated scenario: population block generated by the behavioral adoption ABM "
        f"('{args.source}' sweep means, {entry['seeds']} seeds) per OUTPUT_TO_ADVERSARIAL_ABM.md; "
        f"planning calibration, attacks, and fiscalizer composition unchanged from the base scenario."
    )
    base["population"].update({
        "attentiveShare": round(attentive, 4),
        "salienceShare": round(salience, 4),
        "profileShare": round(profile, 4),
        "passiveShare": round(passive, 4),
        "delegatorShare": round(delegator, 4),
        "delegationBlockSize": block_size,
    })
    base["executors"]["honestShare"] = round(m["honest_executor_share"], 4)
    base["executors"]["opportunisticShare"] = round(m["opportunistic_executor_share"], 4)
    base["behavioralProvenance"] = {
        "experiment": "experiments/behavioral-adoption-abm",
        "source_scenario": args.source,
        "sweep_seeds": entry["seeds"],
        "date": date.today().isoformat(),
        "mapping": "experiments/behavioral-adoption-abm/OUTPUT_TO_ADVERSARIAL_ABM.md",
        "mapped": {
            "attentiveShare": round(attentive, 4),
            "salienceShare": round(salience, 4),
            "profileShare": round(profile, 4),
            "passiveShare": round(passive, 4),
            "delegatorShare": round(delegator, 4),
            "delegationBlockSize": block_size,
            "honestShare": round(m["honest_executor_share"], 4),
        },
        "not_mapped": ["planning signal mixes", "fiscalizer composition", "attacks", "trust dynamics"],
    }

    out = Path(args.output) if args.output else ROOT.parent / "adversarial-abm" / "scenarios" / f"{args.scenario_id}.json"
    out.write_text(json.dumps(base, indent=2) + "\n", encoding="utf-8")
    shares = f"attentive={attentive:.3f} salience={salience:.3f} profile={profile:.3f} delegator={delegator:.3f} passive={passive:.3f}"
    print(f"wrote {out}\n  {shares} blockSize={block_size} honest={m['honest_executor_share']:.3f}")


if __name__ == "__main__":
    main()
